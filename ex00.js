/*

    Précisions avant de commencer:

    ### PROTOTYPES
    
        Désormais dans vos exercices, vous trouverez une section en plus concernant le "prototype".

        Ce prototype, correspond au prototype de la fonction que vous devez créer.
        C'est à dire une description de la fonction telle qu'on l'attend.

        Exemple:

        Prototype:
            str myFunction(param1, param2)

        Cela veut dire que vous devez créer
            une fonction nommé "myFunction"
            qui prend en paramètre "param1" et "param2" (ni plus ni moins)
            et qui retourne une chaine de caractère (str) (ni plus ni moins).

        Voici un exemple d'une fonction qui ne retourne rien, et qui ne prend rien en paramètre:

        Prototype:
        void myFunctionWithoutReturnAndParams()

        Cela veut dire que vous devez créer
            une fonction "myFunctionWithoutReturnAndParams"
            qui ne prend rien en paramètre (ni plus ni moins)
            et qui ne retourne rien (void) (ni plus ni moins).

        Liste des différentes valeurs de retours:

            - str   -> string
            - arr   -> array
            - obj   -> json object
            - nbr   -> number
            - float -> floating number
            - void  -> no return value;

        Vous devez respecter les prototypes.

*/
